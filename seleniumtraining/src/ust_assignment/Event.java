package ust_assignment;

public class Event {
	
	protected String eventNo;
	protected String eventHolderName;
	protected String eventDetail;
	public String getEventNo() {
		return eventNo;
	}
	public void setEventNo(String eventNo) {
		this.eventNo = eventNo;
	}
	public String getEventHolderName() {
		return eventHolderName;
	}
	public void setEventHolderName(String eventHolderName) {
		this.eventHolderName = eventHolderName;
	}
	public String getEventDetail() {
		return eventDetail;
	}
	public void setEventDetail(String eventDetail) {
		this.eventDetail = eventDetail;
	}
	
	
	

}
